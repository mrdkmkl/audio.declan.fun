// ── FFT helpers ──
function refreshFD() { rxAna.getByteFrequencyData(rxFD); }

function peakIn(fLo, fHi) {
  const b0 = Math.max(0, Math.floor(fLo / rxFR));
  const b1 = Math.min(rxFD.length - 1, Math.ceil(fHi / rxFR));
  let maxM = 0, maxB = b0;
  for (let i = b0; i <= b1; i++) { if (rxFD[i] > maxM) { maxM = rxFD[i]; maxB = i; } }
  return { freq: maxB * rxFR, mag: maxM };
}

// ── Simplified state machine ──
// Phases: IDLE → SYNC → DATA → DONE
// 180 Hz detected in DATA = TX pause/resume signal; recalibrate timing when it ends
function rxTick() {
  if (!rxAna || rxUserPaused) return;
  const now = performance.now();
  refreshFD();

  // ── IDLE: wait for 180 Hz start tone ──
  if (rxPhase === 'IDLE') {
    const pk = peakIn(P.SYNC_F - 40, P.SYNC_F + 40);
    if (pk.mag > P.SYNC_TH) {
      if (!rxSyncDet) rxSyncDet = now;
      if (now - rxSyncDet > 80) {
        rxPhase = 'SYNC'; rxSyncGone = 0;
        setRxPhase('START DETECTED', 'Sync carrier locked — awaiting data');
        setRxMsg('Start signal active…');
      }
    } else { rxSyncDet = 0; }
    return;
  }

  // ── SYNC: wait for 180 Hz to end, then begin DATA ──
  if (rxPhase === 'SYNC') {
    const pk = peakIn(P.SYNC_F - 40, P.SYNC_F + 40);
    if (pk.mag < P.SYNC_TH - 12) {
      if (!rxSyncGone) rxSyncGone = now;
      if (now - rxSyncGone > 30) {
        // Sync ended — set up DATA using rxCode
        const cfg = rxCode
          ? { resolution: rxCode.resolution, nColors: rxCode.nColors, timeMs: rxCode.timeMs }
          : { resolution: 32, nColors: 124, timeMs: 50 };  // fallback (shouldn't happen)

        rxDetCFG    = cfg;
        buildPalette(cfg.nColors);
        setupRxCanvas(cfg.resolution);

        rxPhase      = 'DATA';
        rxDataStart  = now + P.PRE_GAP;
        rxLastPx     = -1;
        rxLastSig    = now;
        rxTxPaused   = false;
        rxResumeSyncStart = 0;
        rxResumeSyncGone  = 0;

        setRxPhase(`RECEIVING — ${cfg.resolution}×${cfg.resolution}`,
                   `${cfg.resolution}×${cfg.resolution} · ${cfg.nColors} colors · ${cfg.timeMs}ms/tone`);
        setRxMsg(`Receiving ${(cfg.resolution * cfg.resolution).toLocaleString()} pixels…`);
      }
    } else { rxSyncGone = 0; }
    return;
  }

  // ── DATA: receive pixel tones ──
  if (rxPhase === 'DATA') {
    const cfg    = rxDetCFG;
    const period = cfg.timeMs + P.PX_GAP;

    // ── Check for END tone (above data band) ──
    const endPk = peakIn(P.END_F - 150, P.END_F + 150);
    if (endPk.mag > P.END_TH && endPk.freq > 5200) { finishRxData(); return; }

    // ── Check for RESUME-SYNC (180 Hz in DATA phase = TX paused and now resuming) ──
    const syncPk = peakIn(P.SYNC_F - 40, P.SYNC_F + 40);
    if (syncPk.mag > P.SYNC_TH) {
      if (!rxResumeSyncStart) {
        rxResumeSyncStart = now;
      } else if (now - rxResumeSyncStart > 120) {
        // TX is sending a resume signal
        rxTxPaused = false;
        rxResumeSyncGone = 0;
        setRxMsg('TX resuming…');
      }
      return;  // Don't process data tones while sync is present
    } else if (rxResumeSyncStart > 0) {
      // Sync just ended — recalibrate timing anchor
      if (!rxResumeSyncGone) rxResumeSyncGone = now;
      if (now - rxResumeSyncGone > 20) {
        // Recalibrate: pixel rxPxCnt will arrive after PRE_GAP
        rxDataStart = rxResumeSyncGone + P.PRE_GAP - rxPxCnt * period;
        rxResumeSyncStart = 0;
        rxResumeSyncGone  = 0;
        rxLastPx = rxPxCnt - 1;  // reset so next index is rxPxCnt
        setRxMsg('Receiving…');
        return;
      }
      return;
    }

    // ── Auto-pause detection (TX gone silent) ──
    const anyPk = peakIn(P.FREQ_LO - 100, P.END_F + 200);
    if (anyPk.mag > P.DATA_TH) rxLastSig = now;
    if (now - rxLastSig > 600 && rxPxCnt > 0) {
      if (!rxTxPaused) {
        rxTxPaused = true;
        setRxMsg('TX paused — waiting for resume…');
        setRxPhase('TX PAUSED', 'Waiting for transmitter to resume');
      }
      return;
    }
    if (rxTxPaused) {
      rxTxPaused = false;
      setRxMsg('Receiving…');
      setRxPhase(`RECEIVING — ${cfg.resolution}×${cfg.resolution}`,
                 `${cfg.resolution}×${cfg.resolution} · ${cfg.nColors} colors · ${cfg.timeMs}ms/tone`);
    }

    // ── Normal pixel sampling ──
    const elapsed = now - rxDataStart;
    if (elapsed < 0) return;

    const total  = cfg.resolution * cfg.resolution;
    const pxIdx  = Math.floor(elapsed / period);
    const pxOff  = elapsed % period;
    const lo     = cfg.timeMs * 0.25;
    const hi     = cfg.timeMs * 0.76;

    if (pxOff >= lo && pxOff <= hi && pxIdx !== rxLastPx && pxIdx < total) {
      rxLastPx = pxIdx;
      const pk = peakIn(P.FREQ_LO - 50, P.FREQ_HI + 50);
      if (pk.mag > P.DATA_TH) {
        const ci = freq2idx(pk.freq, cfg.nColors);
        rxPixels[pxIdx] = ci;
        drawRxPixel(pxIdx, ci, cfg.resolution);
        rxPxCnt++;
        $('rxPixelCount').textContent = `${rxPxCnt} / ${total.toLocaleString()} px`;
      }
    }
    if (pxIdx >= total) finishRxData();
  }
}

function finishRxData() {
  rxPhase = 'DONE';
  const total = rxDetCFG ? rxDetCFG.resolution ** 2 : 0;
  setRxPhase('COMPLETE ✓', `${rxPxCnt.toLocaleString()} of ${total.toLocaleString()} pixels received`);
  setRxMsg(`Done — ${rxPxCnt.toLocaleString()} px received`);
  $('imgOutput').classList.add('has-img');
  $('btnPauseRx').disabled = true;
}

