const P = {
  FREQ_LO:   600,    // Hz — data band low
  FREQ_HI:  4500,    // Hz — data band high

  SYNC_F:    180,    // Hz — start / resume carrier
  SYNC_MS:   300,    // ms — start tone duration
  RESUME_MS: 200,    // ms — resume-after-pause tone duration

  END_F:    5500,    // Hz — end marker
  END_MS:    400,    // ms

  PX_GAP:      4,    // ms — silence between pixel tones
  PRE_GAP:    60,    // ms — silence before data starts (after start tone)
  ATT:       0.005,  // s  — tone attack
  REL:       0.005,  // s  — tone release

  // Detection thresholds
  SYNC_TH:    55,    // magnitude for sync detect
  DATA_TH:    28,    // magnitude for data tones
  END_TH:     45,    // magnitude for end tone

  // Lookahead scheduler
  LOOKAHEAD:  0.15,  // s — schedule this far ahead
  SCHED_INT:  25,    // ms — scheduler tick
};

