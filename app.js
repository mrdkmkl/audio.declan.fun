function idx2freq(i, n)  { return P.FREQ_LO + (i / (n - 1)) * (P.FREQ_HI - P.FREQ_LO); }
function freq2idx(f, n)  { return clamp(Math.round((f - P.FREQ_LO) / (P.FREQ_HI - P.FREQ_LO) * (n - 1)), 0, n - 1); }

function fmtTime(ms) {
  if (ms < 1000)   return `${ms.toFixed(0)} ms`;
  if (ms < 60000)  return `${(ms / 1000).toFixed(1)} s`;
  const m = Math.floor(ms / 60000), s = Math.floor((ms % 60000) / 1000);
  if (ms < 3600000) return s ? `${m}m ${s}s` : `${m}m`;
  const h = Math.floor(ms / 3600000), rm = Math.floor((ms % 3600000) / 60000);
  return rm ? `${h}h ${rm}m` : `${h}h`;
}

// ──────────────────────────────────────────
//  CODE ENCODE / DECODE
//  Format: XXXX-XXXX-X  (9 base-36 alphanum + 2 dashes)
//  Group 1 (4): resolution 1–6000   → (res-1) base-36 padded
//  Group 2 (4): nColors + timeMs    → ncIdx*200 + (tm-1)
//  Group 3 (1): checksum            → sum of g1+g2 char values mod 36
// ──────────────────────────────────────────
function encodeCode(res, nc, tm) {
  const g1 = (res - 1).toString(36).toUpperCase().padStart(4, '0');
  const ncIdx = Math.round((clamp(nc, 32, 300) - 32) / 4);
  const tmIdx = clamp(tm - 1, 0, 199);
  const g2 = (ncIdx * 200 + tmIdx).toString(36).toUpperCase().padStart(4, '0');
  let cs = 0;
  for (const c of g1 + g2) cs += parseInt(c, 36);
  const g3 = (cs % 36).toString(36).toUpperCase();
  return `${g1}-${g2}-${g3}`;
}

function decodeCode(raw) {
  const clean = raw.replace(/[-\s]/g, '').toUpperCase();
  if (clean.length !== 9) return null;
  if (!/^[0-9A-Z]{9}$/.test(clean)) return null;

  const g1 = clean.slice(0, 4);
  const g2 = clean.slice(4, 8);
  const g3 = clean.slice(8, 9);

  let cs = 0;
  for (const c of g1 + g2) cs += parseInt(c, 36);
  if ((cs % 36).toString(36).toUpperCase() !== g3) return null;

  const res  = parseInt(g1, 36) + 1;
  const g2v  = parseInt(g2, 36);
  const nc   = Math.floor(g2v / 200) * 4 + 32;
  const tm   = (g2v % 200) + 1;

  if (res < 1 || res > 6000 || nc < 32 || nc > 300 || tm < 1 || tm > 200) return null;
  return { resolution: res, nColors: nc, timeMs: tm };
}

