/* ════════════════════════════════════════════════════════════
   AUDIOPIX — app.js  (Code-System Edition)

   PROTOCOL:
     1. START TONE  — 180 Hz for 300 ms  (sync / "begin" signal)
     2. PRE-GAP     — 60 ms silence
     3. PIXEL DATA  — n*n tones, each toneMs long, gapped by PX_GAP ms
     4. END TONE    — 5500 Hz for 400 ms
     5. RESUME TONE — 180 Hz for 200 ms (sent by TX after a pause/resume)

   Image size, color count, and tone speed are NOT encoded in tones.
   They are exchanged via a 9-character transfer code (XXXX-XXXX-X)
   generated by the transmitter and entered by the receiver.

   TX uses a SINGLE oscillator with a real-time lookahead scheduler.
   This allows true pause/resume without rebuilding the audio graph.
═════════════════════════════════════════════════════════════ */

// ──────────────────────────────────────────
//  PROTOCOL PARAMETERS
