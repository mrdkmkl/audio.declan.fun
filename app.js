function startTx() {
  if (!txPixels) return;
  isTx       = true;
  txPaused   = false;
  txCurrentPixel = 0;

  const btn = $('btnTransmit');
  btn.textContent = 'STOP TX';
  btn.classList.add('stopping');
  $('btnPauseTx').disabled = false;
  $('btnPauseTx').textContent = '⏸ PAUSE';
  $('btnPauseTx').classList.remove('active');
  $('txLed').className = 'led on-tx';
  setTxMsg('Initialising audio…');

  try {
    txAC = new (window.AudioContext || window.webkitAudioContext)();

    const spkId = $('spkSel').value;
    if (spkId && txAC.setSinkId) txAC.setSinkId(spkId).catch(() => {});

    txMasterNode = txAC.createGain();
    txMasterNode.gain.value = 0.88;
    txMasterNode.connect(txAC.destination);

    txOsc = txAC.createOscillator();
    txGainNode = txAC.createGain();
    txOsc.type = 'sine';
    txOsc.connect(txGainNode);
    txGainNode.connect(txMasterNode);
    txGainNode.gain.setValueAtTime(0, 0);
    txOsc.start();

    // Schedule the start tone (180Hz, SYNC_MS)
    let t = txAC.currentTime + 0.06;
    scheduleNote(P.SYNC_F, t, P.SYNC_MS / 1000, 0.88);
    t += P.SYNC_MS / 1000 + P.PRE_GAP / 1000;

    txDataStartAC = t;
    txSchedTime   = t;

    // Start scheduler after start tone finishes
    const startDelay = (P.SYNC_MS + P.PRE_GAP + 80);
    txSchedTimer = setTimeout(() => runTxScheduler(), startDelay);

    startTxSpec();
    setTxMsg('Transmitting…');
    trackTxProgress();

  } catch(err) {
    setTxMsg(`Error: ${err.message}`);
    finishTx(false);
  }
}

function scheduleNote(freq, t0, durS, vol) {
  const attS = P.ATT, relS = P.REL;
  const dur  = Math.max(attS + relS + 0.002, durS);
  txOsc.frequency.setValueAtTime(freq, t0);
  txGainNode.gain.setValueAtTime(0, t0);
  txGainNode.gain.linearRampToValueAtTime(vol, t0 + attS);
  txGainNode.gain.setValueAtTime(vol, t0 + dur - relS);
  txGainNode.gain.linearRampToValueAtTime(0, t0 + dur);
}

function runTxScheduler() {
  if (!isTx || txPaused || !txAC) return;

  const toneS = CFG.timeMs / 1000;
  const gapS  = P.PX_GAP / 1000;
  const nc    = CFG.nColors;
  const now   = txAC.currentTime;
  const ahead = now + P.LOOKAHEAD;

  while (txSchedTime < ahead) {
    if (txCurrentPixel >= txPixels.length) {
      // Schedule end tone
      scheduleNote(P.END_F, txSchedTime, P.END_MS / 1000, 0.72);
      const delay = Math.max(50, (txSchedTime - now + P.END_MS / 1000 + 0.3) * 1000);
      setTimeout(() => finishTx(true), delay);
      txSchedTimer = null;
      return;
    }
    const freq = idx2freq(txPixels[txCurrentPixel], nc);
    scheduleNote(freq, txSchedTime, toneS, 0.86);
    txSchedTime += toneS + gapS;
    txCurrentPixel++;
  }

  txSchedTimer = setTimeout(runTxScheduler, P.SCHED_INT);
}

function togglePauseTx() {
  if (!isTx) return;
  if (txPaused) resumeTx(); else pauseTx();
}

function pauseTx() {
  if (!isTx || txPaused) return;
  txPaused = true;
  clearTimeout(txSchedTimer); txSchedTimer = null;

  // Cancel all future scheduled audio values
  const now = txAC.currentTime + 0.01;
  txGainNode.gain.cancelScheduledValues(now);
  txGainNode.gain.setValueAtTime(0, now);
  txOsc.frequency.cancelScheduledValues(now);

  // Back-calculate which pixel actually played at this moment
  const toneS  = CFG.timeMs / 1000;
  const gapS   = P.PX_GAP / 1000;
  const period = toneS + gapS;
  const elapsed = txAC.currentTime - txDataStartAC;
  const playedSoFar = Math.max(0, Math.min(Math.floor(elapsed / period), txPixels.length));
  txCurrentPixel = playedSoFar;
  // Schedule time will be reset when resuming

  $('btnPauseTx').textContent = '▶ RESUME';
  $('btnPauseTx').classList.add('active');
  $('txLed').className = 'led on-tx paused';
  setTxMsg('Paused — press Resume to continue');
}

function resumeTx() {
  if (!isTx || !txPaused) return;
  txPaused = false;

  const toneS = CFG.timeMs / 1000;
  const gapS  = P.PX_GAP / 1000;
  const period = toneS + gapS;

  // Play a resume-sync tone (180Hz), then continue data
  const t = txAC.currentTime + 0.05;
  scheduleNote(P.SYNC_F, t, P.RESUME_MS / 1000, 0.75);

  // Recalculate data anchor: pixel N will play at t + RESUME_MS + PRE_GAP
  // So txDataStartAC should be set such that pixel txCurrentPixel starts at t+RESUME_MS+PRE_GAP
  txDataStartAC = t + P.RESUME_MS / 1000 + P.PRE_GAP / 1000 - txCurrentPixel * period;
  txSchedTime   = t + P.RESUME_MS / 1000 + P.PRE_GAP / 1000;

  $('btnPauseTx').textContent = '⏸ PAUSE';
  $('btnPauseTx').classList.remove('active');
  $('txLed').className = 'led on-tx';
  setTxMsg('Resuming…');

  const delay = (P.RESUME_MS + P.PRE_GAP + 60);
  txSchedTimer = setTimeout(runTxScheduler, delay);
}

function stopTx() {
  isTx = false;
  txPaused = false;
  clearTimeout(txSchedTimer); txSchedTimer = null;
  if (txOsc) { try { txOsc.stop(); } catch(_) {} txOsc = null; }
  if (txAC)  { txAC.close().catch(() => {}); txAC = null; }
  txGainNode = null; txMasterNode = null;
  finishTx(false);
}

function finishTx(ok) {
  isTx = false; txPaused = false; curTxFreq = 0;
  clearTimeout(txSchedTimer); txSchedTimer = null;
  stopTxSpec();
  if (txAC && ok) { txAC.close().catch(() => {}); txAC = null; }

  const btn = $('btnTransmit');
  btn.textContent = 'TRANSMIT';
  btn.classList.remove('stopping');
  btn.disabled = !txPixels;
  $('btnPreview').disabled = !txPixels;
  $('btnPauseTx').disabled = true;
  $('btnPauseTx').textContent = '⏸ PAUSE';
  $('btnPauseTx').classList.remove('active');
  $('txLed').className = 'led';
  if (!ok) setProgress(0);
}

function trackTxProgress() {
  if (!isTx) return;
  if (!txPaused) {
    const total   = txPixels ? txPixels.length : 1;
    const current = clamp(txCurrentPixel, 0, total);
    const pct     = current / total;

    setProgress(pct);
    const remMs = Math.max(0, (total - current) * (CFG.timeMs + P.PX_GAP));
    $('progDetail').textContent = pct < 1
      ? `${current.toLocaleString()} / ${total.toLocaleString()} px — ${fmtTime(remMs)} left`
      : 'Complete ✓';
    setTxMsg('Transmitting…');

    // Update spectrum freq indicator
    if (current > 0 && current < total) {
      curTxFreq = idx2freq(txPixels[Math.min(current, total-1)], CFG.nColors);
    } else {
      curTxFreq = pct === 0 ? P.SYNC_F : 0;
    }
  }
  requestAnimationFrame(trackTxProgress);
}

